# Metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'Cadena original: {cadena1}')

mayusculas = cadena1.upper #convierte a MAYUSCULA
print(f' Cadena en mayusculas: {mayusculas}')
print(f' Cadena en minusculas: {cadena1.lower()}') # Convierte a MINUSCULA

cadena2 = ' Juan Perez '
print(f' Cadena con espacios: {cadena2}')
print(f' Cadena sin espacios: {cadena2.strip()}') # Elimina ESPACIOS
